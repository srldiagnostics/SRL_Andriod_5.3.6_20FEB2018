package com.srllimited.srl.data;

import java.io.Serializable;

/**
 * Created by RuchiTiwari on 12/22/2016.
 */

public class CollectionData implements Serializable
{

	private String address;

	private String state;

	private String city;

	private String postalCode;

	private boolean isChecked;

	private boolean isLab;

	private String fromDate;

	private String toDate;

	private String time1;

	private String time2;

	private String date1;

	public String getAddress()
	{
		return address;
	}

	public void setAddress(String address)
	{
		this.address = address;
	}

	public String getState()
	{
		return state;
	}

	public void setState(String state)
	{
		this.state = state;
	}

	public String getCity()
	{
		return city;
	}

	public void setCity(String city)
	{
		this.city = city;
	}

	public String getPostalCode()
	{
		return postalCode;
	}

	public void setPostalCode(String postalCode)
	{
		this.postalCode = postalCode;
	}

	public boolean isChecked()
	{
		return isChecked;
	}

	public void setChecked(boolean checked)
	{
		isChecked = checked;
	}

	public boolean isLab()
	{
		return isLab;
	}

	public void setLab(boolean lab)
	{
		isLab = lab;
	}

	public String getFromDate()
	{
		return fromDate;
	}

	public void setFromDate(String fromDate)
	{
		this.fromDate = fromDate;
	}

	public String getToDate()
	{
		return toDate;
	}

	public void setToDate(String toDate)
	{
		this.toDate = toDate;
	}

	public String getTime1()
	{
		return time1;
	}

	public void setTime1(String time1)
	{
		this.time1 = time1;
	}

	public String getTime2()
	{
		return time2;
	}

	public void setTime2(String time2)
	{
		this.time2 = time2;
	}

	public String getDate1()
	{
		return date1;
	}

	public void setDate1(String date1)
	{
		this.date1 = date1;
	}

	@Override
	public String toString()
	{
		return "CollectionData{" + "address='" + address + '\'' + ", state='" + state + '\'' + ", city='" + city + '\''
				+ ", postalCode='" + postalCode + '\'' + ", isChecked=" + isChecked + ", isLab=" + isLab
				+ ", fromDate='" + fromDate + '\'' + ", toDate='" + toDate + '\'' + ", time1='" + time1 + '\''
				+ ", time2='" + time2 + '\'' + ", date1='" + date1 + '\'' + '}';
	}
}
